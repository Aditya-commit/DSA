#include <iostream>
using namespace std;


int main(){

    int a=1 , b=0;

    cout << (a & b) << endl;

    cout << (a | b) << endl;

    cout << (1 ^ 0) << endl;

    cout << (1 ^ 1) << endl;

    cout << (0 ^ 0) << endl;

    cout << (10 << 2) << endl;

    cout << (10 >> 2) << endl;

    return 0;
}